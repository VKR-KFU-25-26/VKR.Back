namespace CourtParser.Models.Entities;

/// <summary>
/// Модель для представления события движения судебного дела
/// Содержит информацию о каждом этапе процесса рассмотрения дела в суде
/// </summary>
public class CourtCaseMovement
{
    /// <summary>
    /// Наименование процессуального события или действия
    /// Примеры: 
    /// - "Регистрация иска (заявления, жалобы) в суде"
    /// - "Передача материалов судье" 
    /// - "Решение вопроса о принятии иска к рассмотрению"
    /// - "Вынесено определение о подготовке дела к судебному разбирательству"
    /// - "Подготовка дела (собеседование)"
    /// - "Назначение дела к слушанию"
    /// - "Судебное заседание"
    /// - "Вынесение решения"
    /// </summary>
    public string? EventName { get; set; } = "";
    
    /// <summary>
    /// Результат или итог процессуального события
    /// Примеры:
    /// - "Иск (заявление, жалоба) принят к производству"
    /// - "Дело назначено к слушанию"
    /// - "Заседание отложено"
    /// - "Ходатайство удовлетворено"
    /// - "Исковые требования удовлетворены"
    /// Может быть пустым, если результат не указан
    /// </summary>
    public string? EventResult { get; set; } = "";
    
    /// <summary>
    /// Правовые основания или нормативные акты, на которых основано событие
    /// Примеры:
    /// - "Статья 133 ГПК РФ"
    /// - "Определение суда от 15.11.2019"
    /// - "Ходатайство истца"
    /// Часто бывает пустым, так как не всегда указывается в открытых данных
    /// </summary>
    public string? Basis { get; set; } = "";
    
    /// <summary>
    /// Дата совершения процессуального события
    /// Является ключевой информацией для построения хронологии дела
    /// </summary>
    public DateTime? EventDate { get; set; }
}